
const max=7;
let star = "";
for (i = 1; i<=max;i++){
    
    if(i%2!==0){
        const diff = max - i;
        const empty = Math.floor(diff/2);
        for(let k = 1; k <= empty; k++){
            star += " ";
        }
        for(let j=1 ; j<=i ; j++) {
            star += "*";
        }
        for(let k = 1; k <= empty; k++){
            star += " ";
        }
        star+="\n";
    }
    
}

console.log(star)
